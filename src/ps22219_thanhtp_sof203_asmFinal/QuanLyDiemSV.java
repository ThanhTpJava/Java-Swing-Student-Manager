    private void LoadData() {
        try {
            list.clear();
            Connection con = DatabaseHelper.oppenConnection();
            PreparedStatement ps = con.prepareStatement("select HoTen, Students.MaSV,TiengAnh,TinHoc,GDTC from Students"
                    + " inner join Grade on Students.MaSV = Grade.MaSV");

            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Grade gd = new Grade();
                //1. gan du lieu cho sv
                gd.setHoten(rs.getString("Hoten"));
                gd.setMasv(rs.getString("MaSV"));
                gd.setTiengAnh(rs.getDouble("TiengAnh"));
                gd.setTinHoc(rs.getDouble("TinHoc"));
                gd.setGDTC(rs.getDouble("GDTC"));
                list.add(gd);
            }
            tblModel = (DefaultTableModel) tblDiemSV.getModel();
            tblModel.setRowCount(0);
            Object[] row;
            for (Grade gd : list) {
                row = new Object[]{gd.getMasv(), gd.getHoten(), gd.getTiengAnh(), gd.getTinHoc(), gd.getGDTC(), gd.getAVG()};
                tblModel.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void DoAVG() {
        index = tblDiemSV.getSelectedRow();
        if (index >= 0) {
            lblDTB.setText(String.valueOf(list.get(index).getAVG()));
        }
    }

