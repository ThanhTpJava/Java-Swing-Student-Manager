    private void updateInfo() {
        tblDiemSV.setRowSelectionInterval(index, index);
        fillTableToForm();
    }

    public void firstSV() {
        if (list.size() != 0) {
            index = 0;
            updateInfo();
        }
    }

    public void lastSV() {
        if (list.size() != 0) {
            index = list.size() - 1;
            updateInfo();
        }
    }

    public void preSV() {
        if (list.size() != 0) {
            if (index == 0) {
                lastSV();
            } else {
                index--;
            }

            updateInfo();
        }
    }

    public void nextSV() {
        if (list.size() != 0) {
            if (index == list.size() - 1) {
                firstSV();
            } else {
                index++;
            }

            updateInfo();
        }
    }

