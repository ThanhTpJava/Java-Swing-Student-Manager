    private boolean ValiDate() {
        if (txtMasv.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập mã sinh viên", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (txtTiengAnh.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập điểm Tiếng Anh", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (Double.parseDouble(txtTiengAnh.getText()) < 0 && Double.parseDouble(txtTiengAnh.getText()) > 10) {
            JOptionPane.showMessageDialog(this, "Điểm Tiếng Anh phải hợp lệ", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (Double.parseDouble(txtTinHoc.getText()) < 0 && Double.parseDouble(txtTinHoc.getText()) > 10) {
            JOptionPane.showMessageDialog(this, "Điểm Tin Học phải hợp lệ", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (Double.parseDouble(txtGDTC.getText()) < 0 && Double.parseDouble(txtGDTC.getText()) > 10) {
            JOptionPane.showMessageDialog(this, "Điểm GDTC phải hợp lệ", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        if (txtTinHoc.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập điểm Tin Học", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        if (txtGDTC.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập điểm GDTC", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

